    cTextura(const char* pathFile);
    ~cTextura();

    int init(SDL_Renderer* pRenderer);
    int render(SDL_Renderer* pRenderer, const SDL_FRect * pdest) const;

    SDL_Texture* getTextura() { return m_pTextura; }
