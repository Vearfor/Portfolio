//--------------------------------------------------------------------------
// Ampliamos  RECT
//--------------------------------------------------------------------------
template <class Val> class cRect
{
public:

    cRect(void);
    ~cRect(void);

    Val getAlto(void);
    Val getAncho(void);

    Val    left;
    Val    top;
    Val    right;
    Val    bottom;
};
//--------------------------------------------------------------------------


//--------------------------------------------------------------------------
// Constructor & Destructor: cRect
//--------------------------------------------------------------------------
template <class Val> cRect<Val>::cRect()
    : top(0)
    , bottom(0)
    , left(0)
    , right(0)
{
}

template <class Val> cRect<Val>::~cRect()
{
}
//--------------------------------------------------------------------------

//--------------------------------------------------------------------------
template <class Val> Val cRect<Val>::getAlto(void)
{
    return (bottom - top);
}

template <class Val> Val cRect<Val>::getAncho(void)
{
    return (right - left);
}
